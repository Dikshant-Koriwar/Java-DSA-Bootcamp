
public class Bit {
    public static void main(String[] args) {

        /*
         * Bitwise operator we have
         * binary and &
         * binary or |
         * binary xor ^
         * binary not ~
         * left shift <<
         * right shift >>
         * unsigned right shift >>
         */
        System.out.println((~(5)) & 1);
    }
}
